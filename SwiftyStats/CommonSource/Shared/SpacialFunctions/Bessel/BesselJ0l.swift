        denom = cn0_8_D()
        ans = polyeval(x: y, coef: num, n: 13) / polyeval(x: y, coef: denom, n: 17) * z
    }
    else if xx >= 8 && xx < 16 {
        z = (xx * xx - r1) * (xx * xx - r2)
        num = cn8_16_N()
        denom = cn8_16_D()
        ans = polyeval(x: xx, coef: num, n: 23) / polyeval(x: xx, coef: denom, n: 23) * z
    }
    return ans
