fileprivate func ak<T: SSFloatingPoint>(k: T, v: T) -> T {
    if k < 0 || v < 0 {
        fatalError()
    }
    if integerPart(k) != k || integerPart(v) != v {
        fatalError()
    }
    let sign: T = pow1(-T.one, k + T.one)
    let tp: T = pow1(pow1(2, k + 1), 2)
    let g: T = pow1(tgamma1(k + T.half), 2)
    let ik: Int = integerValue(k)
    let lkf: T = logFactorial(ik)
    let kf:T = exp1(lkf)
    let d: T = 4 * T.pi * kf * pow1(8, k)
    let ans: T = (sign * tp * g) / d
    return ans
}










